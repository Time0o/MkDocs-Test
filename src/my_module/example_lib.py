"""An example Python module.

It provides functions for adding and dividing numbers.
"""


def add(a: int, b: int) -> int:
    """Add two numbers.

    >>> add(1, 2)
    3

    Args:
        a (int): Summand one.
        b (int): Summand two.

    Returns:
        int: The sum of the summation.

    """
    return a + b
